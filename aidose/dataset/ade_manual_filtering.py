import re
from typing import List


def filter_terms(terms: List[str]) -> List[str]:
    """
    Filters a list of MedDRA terms to isolate those related to dosing,
    administration, and medication use errors.

    This is automatically generated by Google Gemini 2.5 Pro, and the idea was to find a set of explicit filtering
    steps to match the manually curated list of dosing erros from the larger ADE positive terms of MedDRA and by
    experts from Johsnson & Johnson.

    This function programmatically reproduces a manually curated list by applying
    a highly specific set of inclusion and exclusion rules. The patterns are
    designed to match the explicit phrasing of errors in the target list,
    avoiding overly broad keywords that lead to inaccuracies.

    Args:
        terms: The full list of MedDRA terms to filter.

    Returns:
        A sorted list of filtered terms.
    """

    # 1. DEFINE INCLUSION PATTERNS
    include_patterns = [
        # Dosing Amount & Over/Under
        r'overdose', r'underdose', r'overmedication', r'dose calculation error',
        r'incorrect dosage administered', r'incorrect dose administered', r'incorrect dose selected',
        r'extra dose administered', r'increased dose administered',
        r'incomplete dose prescribed', r'insufficient dosage',
        r'prescribed (over|under)dose', r'unadjusted dose',
        r'unapproved dose administered', r'unapproved starting dose administered',  # Made explicit
        r'partial dose delivery',

        # Adherence, Omission & Missed Doses
        r'missed dose', r'missed injection', r'missed step', r'treatment nonadherence',
        r'treatment noncompliance', r'patient ran out of medication', r'vaccine dose omission',
        r'prescribed dosing duration not completed',

        # Administration Timing, Frequency & Duration
        r'administration duration too (long|short)', r'incorrect .* administration duration',
        r'incorrect .* administration rate',
        r'dose administration interval too (long|short)',
        r'(once|scheduled) .* (dose|dosing) taken (less|more) frequently',
        r'drug use .* than labell?ed administration duration',
        r'premature start of scheduled drug administration',
        r'infusion administration interrupted',

        # Accidental / Unintentional Actions
        r'accidental (dose|ingestion|intake|underdose)',
        r'unintentional exposure to product$',  # Made explicit with end-of-string anchor
        r'unintentional exposure to product by child',  # Made explicit
        r'unintentional occupational exposure to product',  # Made explicit
        r'unintended ingestion', r'unintentional use',
        r'unintentional therapeutic error', r'unintentional medical device removal',
        r'unintentional intravascular injection',

        # Preparation, Form, Storage & Dilution
        r'wrong concentration prepared', r'incorrect disposal', r'incorrect storage',
        r'incorrect product dosage form administered', r'incorrect drug dosage form administered',
        r'incorrect product formulation administered', r'vaccine incorrect diluent',
        r'vaccine preparation error',
        r'drug dilution in inappropriate volume of solution',
        r'tablet (crushed|split) incorrectly', r'tablet split by mistake',
        r'unapproved (crushing|splitting)',

        # General & Potential Errors
        r'medication error', r'vaccination error', r'self-administration error', r'drug administration error',
        r'potential for device use error',
        r'potential for medication error',  # Catches "wrong drug" context

        # Device, Technique & Application Errors
        r'mobile medical application use error', r'use of incorrect device control settings',
        r'incorrect needle gauge used', r'multiple use of single-(dose|use) product',
        r'unintentional device misuse', r'unintentional device use beyond',
        r'subcutaneous injection formulation administered by other route',
        r'syringe markings confusion', r'needle priming not performed', r'needle reusage',
        r'injector positioned upside down', r'ocular drug implant use error',
        r'incorrect vaccine administration technique', r'inhalation not administered correctly',

        # Knowledge, Information & System Issues
        r'basal-bolus regimen confusion', r'drug dose prescribing error', r'drug dosage form prescribing error',
        r'drug prescription issue', r'drug titration error', r'drug dose titration not performed',
        r'medication monitoring error', r'post-administration patient monitoring .* performed',
        r'therapeutic drug monitoring analysis', r'organi[s|z]ational systems issue',
        r'.* information in patient.?s record', r'medication history unavailable',
        r'medication knowledge deficit', r'not trained on proper .* use',
        r'misleading product', r'patient misunderstanding', r'patient instructions .* confusing',
        r'use of error-prone abbreviation', r'pharmacy label placed incorrectly',

        # Age Mismatches & Unapproved Use
        r'adult product administered to child', r'child product given to infant',
        r'adult use of a child product', r'drug prescribed for unapproved age group',
        r'product administered to patient of inappropriate age',
        r'drug use for unapproved dosing regimen',

        # Unknown Status
        r'unknown (interval|schedule|route|vaccine product|immuni(s|z)ation)',
        r'uncertain immuni(s|z)ation',

        # Efficacy in Unapproved Use
        r'therapeutic product (in)?effective for unapproved indication',

        # Other Specific Inclusions from Target List
        r'accidental overdose \(therapeutic agent\)',
        r'accidental drug intake by child', r'accidental product intake by child',
        r'poor quality (drug|device|product|vaccine) (administered|used)',
        r'suspected recalled product administered',
        r'out of specification product use', r'paravenous drug administration',
        r'transfusion with incompatible blood', r'medication too hard to chew',
        r'product administered from unauthori[s|z]ed provider',
        r'product administered by wrong person',
        r'product administered at (inappropriate site|compromised administration site)',
        r'prescription drug used',
        r'ventricular drain misplacement',
        r'wet injection'
    ]

    # 2. DEFINE EXCLUSION PATTERNS
    exclude_patterns = [
        # Exclude "wrong drug" unless it is in the context of "Potential for medication error"
        r'(?<!potential for medication error, )wrong drug',
        # Exclude other "wrong entity" errors
        r'wrong patient', r'incorrect patient',

        # Exclude quality issues not involving administration
        r'counterfeit', r'falsified', r'deteriorated',

        # Exclude non-therapeutic/chemical ingestion
        r'nontherapeutic agent or chemical', r'ingestion of chemical', r'desiccant',

        # Exclude "intercepted" errors
        r'intercepted',

        # Exclude toxicity reports
        r'toxicity',

        # Exclude specific, vague, or unwanted overdose terms
        r'^acute overdose$', r'^medically prescribed overdose$',

        # Exclude general route/site errors (specific ones are in the include list)
        r'incorrect route of',
        r'intravenous .* other route', r'rectal .* other route', r'enteral .* other route',
        r'respiratory .* other route', r'intramuscular .* other route', r'intrathecal .* other route',

        # Exclude other specific phrases not in the target list
        r'prolongation of .* treatment duration', r'difficulty dialing',
        r'device difficult', r'dose counter confusion', r'booster dose missed',
        r'loading dose not administered',
        # Broaden the interaction exclusion to catch all types
        r'drug-.* interaction medication error',
        r'circumstance or information capable of leading to'
    ]

    # Compile regex for efficiency and case-insensitivity
    include_regex = re.compile('|'.join(include_patterns), re.IGNORECASE)
    exclude_regex = re.compile('|'.join(exclude_patterns), re.IGNORECASE)

    filtered_terms = []
    for term in terms:
        if include_regex.search(term) and not exclude_regex.search(term):
            filtered_terms.append(term)

    return sorted(filtered_terms)
