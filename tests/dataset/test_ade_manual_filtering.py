class FilterMedDRAUnitTest(unittest.TestCase):
    def setUp(self) -> None:
        if not os.path.exists(MEDDRA_ADE_LABELS_PATH):
            raise FileNotFoundError(f"File {MEDDRA_ADE_LABELS_PATH} not found. Please run the main dataset API first.")
        with open(MEDDRA_ADE_LABELS_PATH, 'r', encoding='utf-8') as f:
            self.full_terms_extracted_from_meddra = json.load(f)["terms"]

        self.expected_terms_from_manual_filtering = MANUALLY_FILTERED_POSITIVE_LABELS_FROM_JJ

    def test_filter_reproduces_expected_terms(self):
        got_filtered = filter_ade_terms_to_focus_on_dosing_errors(self.full_terms_extracted_from_meddra)

        self.assertEqual(set(got_filtered), set(self.expected_terms_from_manual_filtering),
                         "Filtered terms set does not match expected curated set.")
        self.assertEqual(got_filtered, sorted(self.expected_terms_from_manual_filtering),
                         "Filtered terms are not sorted as expected.")
        self.assertGreater(len(got_filtered), 0, "Filtered list should not be empty.")
        self.assertTrue(all(isinstance(t, str) for t in got_filtered),
                        "All filtered terms must be strings.")


if __name__ == "__main__":
    unittest.main()
